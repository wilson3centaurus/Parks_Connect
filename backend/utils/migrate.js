async function seedDefaultThresholds(db) {
  const thresholds = [
    { metric: 'visitors', threshold: 500, comparator: '>' },
    { metric: 'occupancy', threshold: 0.85, comparator: '>' },
    { metric: 'rating_drop_7d', threshold: 3, comparator: '<' },
    { metric: 'incident_high', threshold: 1, comparator: '>=' }
  ];

  for (const threshold of thresholds) {
    const existing = await db.get(
      `SELECT id FROM alert_thresholds WHERE park_id IS NULL AND metric = ?`,
      [threshold.metric]
    );

    if (!existing) {
      await db.run(
        `INSERT INTO alert_thresholds (park_id, metric, threshold, comparator)
         VALUES (NULL, ?, ?, ?)`,
        [threshold.metric, threshold.threshold, threshold.comparator]
      );
    }
  }
}

export async function runMigrations() {
  const db = await getDb();

  for (const sql of schemaStatements) {
    await db.exec(sql);
  }

  for (const sql of dataMaintenanceStatements) {
    await db.exec(sql);
  }

  await seedDefaultThresholds(db);
}

if (process.argv[1] && process.argv[1].includes('migrate.js')) {
  runMigrations()
    .then(() => {
      console.log('Migrations completed.');
    })
    .catch((err) => {
      console.error('Migration failed', err);
      process.exitCode = 1;
    })
    .finally(async () => {
      await closeDb();
    });
}
