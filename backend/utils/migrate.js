/*
Audit summary
Existing Features:
- JWT-authenticated backend with role-based access for authority admins, environment officers, and tourism operators.
- Existing visitor, environmental, incident, feedback, alert, notification, reporting, and dashboard routes.
- Existing mobile feedback + incident submission and seeded demo data.
- Existing web login and dashboard experience with exports and park assignment support.

Missing Features:
- Unified feedback schema fields required by the brief: category, channel, visit_date, updated_at, and public/email ingestion support.
- Public web feedback page at /feedback and backend email feedback endpoint.
- Management alert API semantics required by the brief, including summary_text, triggered_at, manager email targeting, and daily capacity fields on parks.
- Dashboard-oriented feedback filtering/export fields and auto-refresh support data.

Partial Features:
- Alerts exist but currently use legacy severity/status semantics and are partly triggered during normal API requests.
- Analytics dashboard exists but does not yet expose the requested KPI/filter/chart dataset.
- README and automated backend tests do not yet cover the required objectives.
*/
